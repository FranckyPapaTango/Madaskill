    @Test
    @Transactional
    void createCommande() throws Exception {
        int databaseSizeBeforeCreate = commandeRepository.findAll().size();
        // Create the Commande
        restCommandeMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(commande)))
            .andExpect(status().isCreated());

        // Validate the Commande in the database
        List<Commande> commandeList = commandeRepository.findAll();
        assertThat(commandeList).hasSize(databaseSizeBeforeCreate + 1);
        Commande testCommande = commandeList.get(commandeList.size() - 1);
        assertThat(testCommande.getDateCommande()).isEqualTo(DEFAULT_DATE_COMMANDE);
        assertThat(testCommande.getTva()).isEqualTo(DEFAULT_TVA);
        assertThat(testCommande.getTaxesTotales()).isEqualTo(DEFAULT_TAXES_TOTALES);
        assertThat(testCommande.getMontantAmountTtc()).isEqualTo(DEFAULT_MONTANT_AMOUNT_TTC);
        assertThat(testCommande.getIsPayedIsFacture()).isEqualTo(DEFAULT_IS_PAYED_IS_FACTURE);
    }

