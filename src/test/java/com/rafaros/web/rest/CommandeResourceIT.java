    @Test
    @Transactional
    void putExistingCommande() throws Exception {
        // Initialize the database
        commandeRepository.saveAndFlush(commande);

        int databaseSizeBeforeUpdate = commandeRepository.findAll().size();

        // Update the commande
        Commande updatedCommande = commandeRepository.findById(commande.getId()).get();
        // Disconnect from session so that the updates on updatedCommande are not directly saved in db
        em.detach(updatedCommande);
        updatedCommande
            .dateCommande(UPDATED_DATE_COMMANDE)
            .tva(UPDATED_TVA)
            .taxesTotales(UPDATED_TAXES_TOTALES)
            .montantAmountTtc(UPDATED_MONTANT_AMOUNT_TTC)
            .isPayedIsFacture(UPDATED_IS_PAYED_IS_FACTURE);

        restCommandeMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedCommande.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedCommande))
            )
            .andExpect(status().isOk());

        // Validate the Commande in the database
        List<Commande> commandeList = commandeRepository.findAll();
        assertThat(commandeList).hasSize(databaseSizeBeforeUpdate);
        Commande testCommande = commandeList.get(commandeList.size() - 1);
        assertThat(testCommande.getDateCommande()).isEqualTo(UPDATED_DATE_COMMANDE);
        assertThat(testCommande.getTva()).isEqualTo(UPDATED_TVA);
        assertThat(testCommande.getTaxesTotales()).isEqualTo(UPDATED_TAXES_TOTALES);
        assertThat(testCommande.getMontantAmountTtc()).isEqualTo(UPDATED_MONTANT_AMOUNT_TTC);
        assertThat(testCommande.getIsPayedIsFacture()).isEqualTo(UPDATED_IS_PAYED_IS_FACTURE);
    }

